/**
 * PixelSprite — pixel art SVG icon
 * @param {string}  name    - sprite key (book, gamepad, africa, community, globe, trophy, lightning, scroll, calendar, mail, star)
 * @param {string}  colour  - fill colour (CSS string)
 * @param {number}  size    - rendered pixel size (default 24 = 3px per cell × 8 cells)
 * @param {string}  className
 */
export default function PixelSprite({ name, colour = '#F5A623', size = 24, className }) {
  const grid = SPRITES[name]
  if (!grid) return null

  const rows = grid.length
  const cols = grid[0].length
  const cellSize = size / 8  // scale to fit in `size` × `size` box

  return (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${cols} ${rows}`}
      xmlns="http://www.w3.org/2000/svg"
      className={className}
      aria-hidden="true"
      style={{ imageRendering: 'pixelated', display: 'block' }}
    >
      {grid.flatMap((row, ri) =>
        row.map((cell, ci) =>
          cell ? (
            <rect
              key={`${ri}-${ci}`}
              x={ci}
              y={ri}
              width={1}
              height={1}
              fill={colour}
            />
          ) : null
        )
      )}
    </svg>
  )
}
